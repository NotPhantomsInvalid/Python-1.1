#Variables in python are very simple, you can declare a variable and assign a value to it in the same line
#For example:
a = 5
#This line declares a variable called "a" and assigns the value 5 to it
#You can also declare multiple variables in the same line
a,b,c = 5,10,15
#This line declares three variables called a,b and c and assigns the values 5,10 and 15 to them respectively
#You can also declare a variable without assigning a value to it
a = None
#This line declares a variable called "a" and assigns the value None to it
