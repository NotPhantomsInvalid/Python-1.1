#Function on python
#Functions in python are very simple, you can declare a function using the "def" keyword
#For example:
def sumar(a, b):
    resultado = a + b
    print(f"La suma de {a} y {b} es {resultado}")
sumar(5, 3) 
#This code declares a function called "sumar" that receives two parameters "a" and "b" and returns the sum of them
def restar(a, b):
    resultadoresta = a - b
    print(f"La resta de {a} y {b} es {resultadoresta}")
restar (4, 2)
    



