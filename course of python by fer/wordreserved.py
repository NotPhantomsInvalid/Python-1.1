#Word reserved for declaring a variable

#List of reserved words in Python
a=list("Hello World")
print(a)
print("______________________")
#If of reserved words in Python
palabra="Python"
if palabra == "Python":
    print("Python is the best")
elif palabra == "Java":
    print("Java is the best")
else:
    print("I don't know")
print("______________________")
#While of reserved words in Python
b=0
while b < 4:
    print(b)
    b+=1
print("______________________")
#For of reserved words in Python
for i in range(4):
    print(i)
print("______________________")
#continue of reserved words in Python
for examplecontinue in range(3):
    if examplecontinue == 1:
        continue
    print(examplecontinue)
print("______________________")
#break of reserved words in Python
c=0
while True:
    print(c)
    if c == 5:
        break
    c+=1
print("______________________")
#False, True, None of reserved words in Python
d=False
e=True
f=None
print(d,e,f)
print("______________________")
#False example
